#!/usr/bin/env python3

string = ("This is a bad word!")

print(string[2:5]) # This will print from index 2 to up to 5(not included)

#Can be done with list of anything as well as tuples.

lisst = ( "Josh", "Don", "Steve", "Cy", "jarret", "patrick", "jt", )
